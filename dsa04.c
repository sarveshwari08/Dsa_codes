#include<stdio.h>
int main(){
    int i,n,temp;
    scanf("%d",&n);
    int arr[n];
    for(i=0;i<n;i++){
        scanf("%d",&arr[i]);
    }
    for(i=0;i<(n/2);i++){
        int *k = &arr[i];
        int *p= &arr[n-1-i];
        temp = *k;
        *k = *p;
        *p = temp;
    }
    for(i=0;i<n;i++){
        printf("%d ",arr[i]);
    }
}